def get_maze_data(file_path):
    start = (0, 0)

    with open(file_path) as fp:
        grid = fp.read().split("\n")
        grid = [i for j, i in enumerate(grid) if j < 25]

    i = 0
    for l in grid:
        new_l = ''
        j = 0
        for c in l:
            if c != '.' and c != 'o' and c != 'u':
                new_l = new_l + '1'
            elif c == 'o':
                new_l = new_l + '2'
            elif c == 'u':
                new_l = new_l + '0'
                start = (i, j)
                # print start
            else:
                new_l = new_l + '0'
            grid[i] = list(map(int, new_l))
            j = j + 1
        i = i + 1
    # print grid

    return start, grid
# 0 for road
# 1 for wall
# 2 for target nodes

